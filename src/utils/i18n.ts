export const changeLanguage = async (language: string) => {
  try {
    await AsyncStorage.setItem('user-language', language);

    // Handle RTL for Hebrew
    const isRTL = language === 'he';
    I18nManager.allowRTL(true);

    if (I18nManager.isRTL !== isRTL) {
      I18nManager.forceRTL(isRTL);
      // Note: In a real app, you might want to restart/reload here
      // For now, we'll just update the language
    }

    i18n.changeLanguage(language);
  } catch (error) {
    console.log('Error saving language:', error);
  }
};

// Get current text direction
export const getTextDirection = () => {
  return I18nManager.isRTL ? 'rtl' : 'ltr';
};

// Helper for RTL-aware styles
export const rtlStyle = (ltrStyle: object, rtlStyle: object) => {
  return I18nManager.isRTL ? rtlStyle : ltrStyle;
};

